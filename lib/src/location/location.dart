///判断GPS坐标是否在中国
bool isInsideChina(double latitude, double longitude)
{
	return (isInsidePolygon(list1, latitude, longitude)
			|| isInsidePolygon(list2, latitude, longitude)
			|| isInsidePolygon(list3, latitude, longitude)
			|| isInsidePolygon(list4, latitude, longitude));
}

///
/// 判断GPS坐标是否在多边形中
/// @param List  list 国家行政边界数组，[纬度,经度] 的形式，取0,1键值
/// @param float lat  纬度
/// @param float lng  经度
/// @return bool
///
bool isInsidePolygon(List<_LatLng> list, double lat, double lng)
{
	var inside = false;
	var prePoint = list[0];
	for(var i = 1; i < list.length; i++) {
		var nextPoint = list[i];
		if(lng > Math.min(prePoint.longitude, nextPoint.longitude) && lng <= Math.max(prePoint.longitude, nextPoint.longitude) && lat <= Math.max(prePoint.latitude, nextPoint.latitude) && prePoint.longitude != nextPoint.longitude) {
			var xinters = (lng - prePoint.longitude) * (nextPoint.latitude - prePoint.latitude) / (nextPoint.longitude - prePoint.longitude) + prePoint.latitude;
			if(prePoint.latitude == nextPoint.latitude || lat <= xinters)
			inside ^= true;
		}
		prePoint = nextPoint;
	}
	return inside;
}

